package com.example.myminiodrive;

public class Objects {

    private String name;
    private Float size;

    public Objects(String name, Float size) {
        this.name = name;
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getSize() {
        return size;
    }

    public void setSize(Float size) {
        this.size = size;
    }

}
